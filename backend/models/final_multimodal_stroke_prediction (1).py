def train_model(epochs=10):
    for epoch in range(epochs):

        model.train()
        total_loss = 0
        correct = 0
        total = 0

        for mri_imgs, ct_imgs, labels in train_loader:

            mri_imgs = mri_imgs.to(device)
            ct_imgs  = ct_imgs.to(device)
            labels   = labels.to(device)

            optimizer.zero_grad()

            outputs = model(mri_imgs, ct_imgs)
            loss = criterion(outputs, labels)

            loss.backward()
            optimizer.step()

            total_loss += loss.item()

            _, preds = torch.max(outputs, 1)
            correct += (preds == labels).sum().item()
            total += labels.size(0)

        acc = correct / total

        print(f"Epoch {epoch+1}")
        print(f"Loss: {total_loss:.4f} | Accuracy: {acc:.4f}")


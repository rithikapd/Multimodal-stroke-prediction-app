class MultimodalModel(nn.Module):
    def __init__(self):
        super().__init__()

        # MRI model
        self.mri_model = models.resnet50(pretrained=True)
        mri_features = self.mri_model.fc.in_features
        self.mri_model.fc = nn.Identity()

        # CT model
        self.ct_model = models.resnet50(pretrained=True)
        ct_features = self.ct_model.fc.in_features
        self.ct_model.fc = nn.Identity()

        # Fusion
        self.classifier = nn.Sequential(
            nn.Linear(mri_features + ct_features, 256),
            nn.ReLU(),
            nn.Dropout(0.5),
            nn.Linear(256, 2)
        )

    def forward(self, mri, ct):
        mri_feat = self.mri_model(mri)
        ct_feat  = self.ct_model(ct)

        combined = torch.cat((mri_feat, ct_feat), dim=1)
        return self.classifier(combined)


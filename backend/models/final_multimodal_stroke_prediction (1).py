def compute_bertscore(pred_text, ref_text):
    P, R, F1 = score([pred_text], [ref_text], lang="en", verbose=False)
    return F1.mean().item()

# -------- RadGraph (Simple Version) --------
def compute_radgraph(pred_text, ref_text):
    keywords = ["stroke", "lesion", "hemorrhage", "infarction"]

    pred_hits = sum([1 for k in keywords if k in pred_text.lower()])
    ref_hits  = sum([1 for k in keywords if k in ref_text.lower()])

    if ref_hits == 0:
        return 0

    return pred_hits / ref_hits

# -------- COMBINED --------
def compute_all_metrics(pred_text, ref_text):
    bert = compute_bertscore(pred_text, ref_text)
    radgraph = compute_radgraph(pred_text, ref_text)
    return bert, radgraph

